    void findRowAndColIndex(ArrayList<ArrayList<Integer>> list) {
        //take indexRow and indexCol as arrayList
        boolean[] indexRowArr = new boolean[list.size()];
        boolean[] indexColArr = new boolean[list.get(0).size()];

        for (int i = 0; i < list.size(); i++) {
            for (int j = 0; j < list.get(i).size(); j++) {
                if (list.get(i).get(j) == 0) {
                    //add to row and col list
                    indexRowArr[i] = true;
                    indexColArr[j] = true;
                }
            }
        }

        System.out.println("indexRowArr");
        for (boolean bool : indexRowArr) {
            System.out.print(bool + ", ");
        }

        System.out.println("\n");

        System.out.println("indexColArr");
        for (boolean bool : indexColArr) {
            System.out.print(bool + ", ");
        }

        System.out.println("\n");


        //change items to zero
        for (int i = 0; i < list.size(); i++) {
            for (int j = 0; j < list.get(i).size(); j++) {
                if (indexRowArr[i] == true || indexColArr[j] == true) {
                    list.get(i).set(j, 0);
                }
            }
        }


        System.out.println(list);
    }


